

# Entrada das notas
nota1 = float(input("Digite a primeira nota (peso 2): "))
nota2 = float(input("Digite a segunda nota (peso 3): "))

# Pesos
peso1 = 2
peso2 = 3

# Cálculo da média ponderada
media_ponderada = (nota1 * peso1 + nota2 * peso2) / (peso1 + peso2)

# Saída
print("A média ponderada é:", media_ponderada)

